# Built in Modules

# Math module
import math

print(math.pi)
print(math.e)
print(math.factorial(5))
print(math.ceil(4.89))
print(math.floor(5.9))

# random module
import random

print(random.randint(1,100))
a = [1,2,3,4,5]
print(random.shuffle(a))

# time module
import time

print(time.time())
print(time.ctime())
# waits or delays next step in the code
print(time.sleep(1))

# os module
import os

print(os.getcwd())
print(os.listdir())



