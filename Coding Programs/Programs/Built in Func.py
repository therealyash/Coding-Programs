# Built in function


# Modulus function
print(abs(4))
print(abs(-4))

# Power Function
# It is 2 to the power 3 which is 8
print(pow(2,3))


# Div/Mod Function
# // - integer division
# % - modulus
# divmod returns integer division and modulus in a tuple
print(divmod(5,2))

# bin/oct/hex
print(bin(4))
print(oct(4))
print(hex(4))


# id function
# used to retrieve address of the variable
a = 3
print(id(a))

# ord function
# returns ascii code
print(ord('a'))


# help
# documentation of the given function
help('sum')
help('print')




